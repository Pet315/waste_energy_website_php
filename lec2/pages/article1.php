        <h2>Пожежа на одному зі сміттєзвалищ Кувейту</h2>
        <p class="date">18.02.2022</p>
        <img class="calendar" src="pages/images/icons/calendar.png">

        <p class="par1">У Кувейті горить найбільше у світі звалище автомобільних шин, повідомляють місцеві ЗМІ. Очевидці 
            публікують фотографії та відео пожежі та зазначають, що гігантський стовп диму видно здалеку, і, судячи з фотографій 
            із Twitter, навіть із космосу. Екологи вже б'ють на сполох: якщо вогонь не вдасться погасити найближчим часом, то це може 
            завдати значної шкоди природі, тому що при горінні старих шин відбувається виділення небезпечних речовин: найсильніших 
            канцерогенів біфеніла та бенз(а)пірена, а також діоксину, фурану, антрацену. та свинцю.
        </p>

        <img class="image1" src="pages/images/kuwait_fire1.webp">
        <p1 class="image_link1">© FA Climate, Weather and Atmosphere / YouTube.com</p1>

        <p class="par2">Ліза Будріна: "Пожежа почалася 3 серпня; стовпи чорного отруйного диму, що піднялися високо в небо, видно здалеку. 
            Поки охоплено порівняно невелику частину кладовища шин, що займає площу 600 тисяч квадратних метрів», — передають очевидці. 
            У Мережі також з'явилося відео інциденту від анонімного автора. YouTube-канал FA Climate, Weather and Atmosphere, що перезалив ролик, 
            б'є на сполох: пожежа загрожує екологічною катастрофою, відео було знято на кладовищі покришок у Сулайбії, яке вважається найбільшим 
            у світі — там зберігається понад 52 мільйони шин. , на яких дійсно видно дим над звалищем."</p>
        
        <img class="image2" src="pages/images/kuw_fire2.jpg">
        <p1 class="image_link2">4kolesa.mirtesen.ru</p1>

        <p class="par3">Це вже не перший подібний випадок цього року. Так, за даними Xinhuanet.com, у квітні цього року спалахнули покришки на 
            цвинтарі в мухафазі (провінції) Джахра. Було висунуто версію, що причиною став підпал.
        </p>

